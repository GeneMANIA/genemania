package org.genemania.exception;

public class NoUserNetworkException extends Exception {

	private long id;
	private String name;

	public NoUserNetworkException(long id, String name) {
		super("No uploaded network found with ID " + id + " and name `" + name
				+ "`");
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
